function star1click() {
    var star1 = document.getElementById("s1");
    var computedStyle = window.getComputedStyle(star1);

    if (computedStyle.color === "rgb(138, 138, 138)") {
        star1.style.color = "rgb(255, 217, 0)";
    } else {
        star1.style.color = "rgb(138, 138, 138)";
    }
}

